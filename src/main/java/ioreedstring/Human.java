package ioreedstring;

public class Human {

    private String FirstName;
    private String SecondName;

    public Human(String firstName, String secondName) {
        FirstName = firstName;
        SecondName = secondName;
    }

    public String getFirstName() {
        return FirstName;
    }

    public String getSecondName() {
        return SecondName;
    }
}
